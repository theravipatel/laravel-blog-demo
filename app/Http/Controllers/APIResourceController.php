    {
        $res = 0;
        $msg = "Something went wrong. Please try again.";
        if(isset($req)){
            $cat_data = new Category;
            $cat_data->name        = $req->name;
            $result = $cat_data->save();
            if($result){
                $res = 1;
                $msg = "Data saved successfully.";
            }
        }

        return ["result"=>$res,"msg"=>$msg];
    }

